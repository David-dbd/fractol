#include "libft.h"

void	ft_bzero(void *ptr, size_t len)
{
	unsigned char	*d;
	size_t			i;

	i = 0;
	d = (unsigned char *)ptr;
	while (i < len)
	{
		d[i] = 0;
		i++;
	}
}
